<h3><strong><span lang="en-US">2. </span></strong><strong>Оптимизируйте использование изображений</strong> </h3>
 	 
<p>Изображения часто кажутся одним из самых простых элементов сайта, но их использование так же можно оптимизировать. Каждому изображению соответствует имя файла и аттрибут &quot;alt&quot;, оба эти элемента полезны.</p>
 
<p>Аттрибут &quot;alt&quot; позволяет ввести текст, который будет отображаться, если изображение по какой-либо причине невозможно показать.</p>
 
<p>Зачем используется этот аттрибут? Если пользователь просматривает ваш сайт через браузер с отключенными картинками или использует программу 	чтения текста с экрана, alt-текст будет служить описанием изображения.</p>
 
<p>Так же alt-текст используется в графических ссылках, в таком случае он обрабатывается поисковыми системами как анкорный текст ссылки. Несмотря на это, мы не советуем использовать большое количество графических ссылок для навигации по вашему сайту, так как текстовые ссылки справляются с этой ролью не хуже. Ну и, наконец, грамотное описание изображения в Теге alt вместе с информативным именем файла помогают системам поиска картинок, таким как &quot;Поиск Картинок Google&quot;, проиндексировать изображения с вашего сайта.</p>
 
<br />
 
<h3><strong><span lang="en-US">3. </span></strong><strong>Анонсы должны быть уникальными, а не вырезкой из полного описания</strong> </h3>
 
<p>Качественный и конкурентоспособный контент – это то, что способно повлиять на позиции вашего сайта гораздо сильнее чем все вышеописанные факторы. Пользователи знают толк в качественных сайтах, и могут рекомендовать понравившийся сайт другим пользователям в блогах, форумах, социальных сетях, электронных 	письмах. Естественная реклама и рекомендации от одного пользователя к другому делают репутацию вашему сайту как среди пользователей, так и поисковых машин. Добиться этого эффекта без качественного контента невозможно. </p>
 
<p><b><em>Создавайте новый, уникальный контент</em></b>. </p>
 
<p>Новый контент заставит лояльных пользователей возвращаться на ваш сайт снова и снова, а также приведет новых пользователей.</p>
 
<p><font color="#ee1d24">Не советуем:</font></p>
 
<ul> 
  <li> дублировать анонсы и полное описание материала; </li>
 
  <li> собирать (или даже просто копировать) существующий контент, не несущий никакой дополнительной информации для пользователей; </li>
 
  <li> наполнять сайт частично или полностью скопированными друг с друга страницами. </li>
 </ul>

<p><strong>Внимание!</strong> На данном сайте размещено демонстрационное информационное наполнение, не предназначенное для публикации в сети Интернет.</p>

<p>Материалы предназначены исключительно для демонстрации возможностей сайта и являются справочной информацией для подготовки уникальных текстов и иллюстраций. Кроме того это необходимо для более успешного продвижения сайта. Советуем заменить демонстрационное наполнение на уникальное, соответствующее вашей организации.</p>

 
