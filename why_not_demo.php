<h3><strong><span lang="en-US">4. </span></strong><strong>Для лучшей индексации стоит сформировать файл sitemap.xml</strong> </h3>
  
<p>Sitemaps — XML-файл с информацией для поисковых систем (таких как Яндекс, Google, Yahoo, Ask.com, Bing) о страницах веб-сайта, которые подлежат индексации. Sitemaps может помочь поисковикам определить местонахождение страниц сайта, время их последнего обновления, частоту обновления и важность относительно других страниц сайта для того, чтобы поисковая машина смогла более разумно индексировать сайт.</p>
<p>Сформировать файл sitemap.xml можно в административном разделе сайта. Для этого необходимо зайти в меню "Настройки" далее выбрать "Поиск" и здесь выбрать необходимые параметры.</p>
<br />
 
<h3><strong><span lang="en-US">5. </span></strong><strong>Важно заполнить корректные теги/ключевые слова</strong> </h3>
 
<p>Теги — это необязательные атрибуты, размещенные в заголовке страницы, которые могут содержать ее описание, ключевые слова к ней, информацию об авторе, управляющие команды для броузера и поисковых роботов, и прочую служебную информацию, не предназначенную для посетителей. Многие из поисковых систем на сегодняшний день активно используют теги. Использование тегов позволит поисковому роботу правильно и качественно проиндексировать ваш сайт.</p>
 
<br />
 
<h3><strong><span lang="en-US">6. </span></strong><strong>Пишите релевантный анкорный текст</strong> </h3>
 
<p>Анкорный текст – это текст, который 	пользователь видит в качестве описания 	ссылки, он помещается в тег &lt;a 	href=&quot;...&quot;&gt;<i>анкорный текст</i>&lt;/a&gt;</p>
 
<p>Анкорный текст рассказывает Google о странице, на которую вы ссылаетесь. Ссылки на странице могут быть внутренними (ссылаться на другие страницы сайта), или внешними (ведущими на другие сайты). В любом случае, чем лучше ваш анкорный текст – тем легче пользователю ориентироваться, и тем понятнее поисковой системе, о чем страница, на которую вы ссылаетесь.</p>
 
<p><b><em>Как правильно использовать анкорный текст</em></b></p>
 
<p><em>Выбирайте информативный текст</em> – Анкорный текст должен содержать хотя бы краткую информацию о странице по ссылке.</p>
 
<p><font color="#ee1d24">Не советуем:</font></p>
 
<ul> 
  <li> использовать в качестве анкорного текста общие слова вроде &quot;страница&quot;, &quot;статья&quot;, или &quot;кликните здесь&quot;; </li>
 
  <li> использовать анкорный текст, не имеющий никакого отношения к материалу по ссылке; </li>
 
  <li> использовать URL страницы в качестве анкорного текста (хотя возможны исключения, например, если нужно прорекламировать новый адрес сайта). </li>
 </ul>
 
<p><b><em>Стремитесь к краткости</em></b>. </p>
 
<p>Ваша цель – короткий и информативный текст, обычно одна точная фраза.</p>
 
<p><font color="#ee1d24">Не советуем:</font></p>
 
<ul> 
  <li> писать длинный анкорный текст, например целое предложение или параграф. </li>
 </ul>
 
<p><b><em>Используйте узнаваемый формат ссылок</em></b>. </p>
 
<p>Сделайте анкорный текст ссылок визуально отличимым от обычного текста вашего сайта. Пользователя вводят в заблуждение малозаметные ссылки или ссылки, на которые он может нажать случайно.</p>
 
<p><font color="#ee1d24">Не советуем:</font></p>
 
<ul> 
  <li> использовать для анкорного текста ссылок стиль, визуально неотличимый от обычного текста страницы. </li>
 </ul>
 
<p><b><em>Не забывайте про анкорный текст для внутренних ссылок</em></b> – возможно, вы уделяете внимание прежде всего ссылкам, указывающим на другие вебсайты, но внутренние ссылки не менее важны, так как они помогают Google лучше сканировать ваш сайт.</p>
 
<p><font color="#ee1d24">Не советуем:</font></p>
 
<ul> 
  <li> наполнять анкорный текст большим количеством ключевых слов; </li>
 
  <li> ставить лишние ссылки, не имеющие навигационной ценности для пользователей. </li>
 </ul>
