<h3><strong><span lang="en-US">1. </span></strong><strong>В тегах заголовков не должно быть картинок и ссылок</strong> 
  <br />
 </h3>
 
<p> Теги заголовков (не путать с html тегами или заголовками протокола HTTP ) используются для разметки структуры сайта для пользователей. Есть 6 размеров заголовков, начиная с самого важного h1 и заканчивая наименее важным h6.   
  <br />
 Текст в тегах заголовков как правило крупнее чем обычный текст страницы, что служит визуальной подсказкой пользователю о его важности и тематике контента под этим заголовком. Заголовки разных размеров позволяют создать иерархическую структуру содержимого сайта и улучшить навигацию на странице. 
  <br />
 <em> 
    <br />
   <strong>Как правильно использовать заголовки</strong></em> 
  <br />
 </p>
  
<p>Представьте что вы пишете тезисы. Подобно тезисам в статье или газете, текст вашей страницы имеет основную тему и подпункты, ее развивающие. Продумайте их и отметьте с помощью 		разных заголовков.</p>
  
<p><font color="#ee1d24">Не советуем:</font>  </p>
 
<ul>  
  <li> размещать в тегах заголовков текст, не имеющий навигационной ценности; </li>
 
  <li> использовать теги заголовков там, где больше подошли бы теги &lt;em&gt; или &lt;strong&gt;; </li>
 
  <li> беспорядочно переключаться от 			заголовков одного размера к другому; </li>
<li> многократно использовать заголовок h1 на одной странице сайта, поскольку он используется как заголовок целого документа и многократное его использование может привести к неправильной индексации страниц. </li>
 </ul>
 
<p></p>
 
<p><b><em>Не разбрасывайтесь заголовками</em></b></p>
 
<p>Используйте заголовки только там, где они действительно необходимы. Слишком большое количество заголовков на странице дезориентирует пользователя и затрудняет понимание того, где заканчивается одна тема и начинается другая.</p>
  
<p><font color="#ee1d24">Не советуем:</font></p>
  
<ul>  
  <li> засорять страницу лишними заголовками; </li>
 
  <li> помещать весь текст страницы в тег заголовка; </li>
 
  <li> использовать заголовки только для стилизации текста, без определения структуры страницы. </li>
 </ul>
 	 
